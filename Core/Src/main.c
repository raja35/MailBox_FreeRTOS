void vTask_Receiver_Handler(void * Param)
{
	uint8_t rxBuff = 0;
	while(1)
	{
		if(!HAL_GPIO_ReadPin(GPIOE, K1_BTN_Pin))
		{
			//do nothing
		}
		else
		{
			xQueueReceive(xMailbox, &rxBuff, portMAX_DELAY);
			if(0 == rxBuff)
			{
				  HAL_GPIO_WritePin(GPIOA,D2_LED_Pin , GPIO_PIN_SET);

			}
			else
			{
				  HAL_GPIO_WritePin(GPIOA,D2_LED_Pin , GPIO_PIN_RESET);
			}
		}
	}
}

void vTask_Sender_Handler(void * Param)
{
	uint8_t txBuff = 0;
	while(1)
	{
		HAL_GPIO_WritePin(GPIOA,D3_LED_Pin , GPIO_PIN_SET);
		vTaskDelay(100);
		HAL_GPIO_WritePin(GPIOA,D3_LED_Pin , GPIO_PIN_RESET);
		vTaskDelay(100);
		txBuff = (txBuff + 1)%2;
		xQueueSendToBack( xMailbox, &txBuff, portMAX_DELAY);
		vTaskDelay(2000);

	}
}

